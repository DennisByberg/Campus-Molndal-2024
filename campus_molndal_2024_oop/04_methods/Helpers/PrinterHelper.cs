using System;

namespace campus_molndal_2024_oop._04_methods
{
    public static class PrinterHelper
    {
        public static void Print(string x)
        {
            Console.WriteLine(x);
        }

        public static void Print(int x)
        {
            Console.WriteLine(x);
        }

        public static void Print(double x)
        {
            Console.WriteLine(x);
        }
    }
}
